function gearIcon(x,y,r,col){
cx.save();
cx.scale(S,S);
cx.translate(x,y);
cx.fillStyle=col;
cx.beginPath();
for(let i=0;i<8;i++){
const a=i*Math.PI/4;
cx.save();
cx.rotate(a);
cx.rect(-r*0.16,-r,r*0.32,r*0.34);
cx.restore();
}
cx.arc(0,0,r*0.72,0,Math.PI*2);
cx.fill();
cx.globalCompositeOperation="destination-out";
cx.beginPath();
cx.arc(0,0,r*0.3,0,Math.PI*2);
cx.fill();
cx.restore();
}
